/// Human explanations for "Зачем это нужно?" - two short sentences per
/// question: why we ask, and what it changes in the agreement. Written
/// like a good consultant, never like a lawyer: no legal boilerplate, no
/// "существенное условие", no "переход права собственности".
///
/// The question text itself comes from the backend planner; this matcher
/// only picks which explanation fits it, by keywords, most specific
/// first. A question nothing matches gets the calm generic fallback -
/// still human, never legalese.
abstract final class QuestionExplanations {
  static String forQuestion(String question) {
    final q = question.toLowerCase();

    bool has(List<String> keywords) => keywords.any(q.contains);

    // --- Money (specific before generic) ---
    if (has(['предоплат', 'аванс', 'задаток'])) {
      return 'Если будет предоплата, мы добавим это условие в договор. '
          'Тогда обе стороны будут понимать, какую сумму и когда нужно оплатить.';
    }
    if (has(['способ оплат', 'как будет произведена оплата', 'как оплат', 'порядок оплат', 'наличн', 'безналичн'])) {
      return 'Нам нужно указать способ оплаты. '
          'Это поможет точно описать порядок расчёта в договоре.';
    }
    if (has(['счёт', 'счет', 'реквизит', 'мфо', 'iban'])) {
      return 'Мы спрашиваем реквизиты, чтобы деньги ушли на правильный счёт. '
          'Они будут указаны в разделе об оплате.';
    }
    if (has(['процент', 'ставк'])) {
      return 'Мы уточняем процент, чтобы обе стороны одинаково понимали условия. '
          'Он будет прописан в договоре вместе с суммой.';
    }
    if (has(['зарплат', 'оклад', 'вознагражден'])) {
      return 'Мы спрашиваем размер оплаты, чтобы он был зафиксирован письменно. '
          'Эта сумма будет указана в договоре.';
    }
    if (has(['цена', 'цену', 'стоимост', 'сумм'])) {
      return 'Мы указываем цену, чтобы обе стороны одинаково понимали стоимость сделки. '
          'Эта сумма будет внесена в договор.';
    }

    // --- Time ---
    if (has(['дата передачи', 'когда передадите', 'когда передать', 'когда планируете передать', 'срок передачи'])) {
      return 'Нам нужно знать дату передачи. '
          'Она будет указана в договоре и поможет избежать споров о сроках.';
    }
    if (has(['срок возврата', 'когда вернут', 'вернуть заём', 'вернуть займ', 'погашен'])) {
      return 'Мы уточняем, когда нужно вернуть деньги. '
          'Эта дата будет указана в договоре, чтобы обе стороны понимали срок.';
    }
    if (has(['дата выхода', 'начала работы', 'приступит к работе', 'первый рабочий'])) {
      return 'Мы спрашиваем дату начала работы, чтобы зафиксировать её письменно. '
          'С этого дня начнут действовать договорённости.';
    }
    if (has(['срок', 'период', 'до какого', 'длительн'])) {
      return 'Мы уточняем срок, чтобы обе стороны понимали, до какого момента действуют договорённости. '
          'Эти даты будут указаны в договоре.';
    }
    if (has(['дата', 'когда'])) {
      return 'Мы спрашиваем дату, чтобы зафиксировать её письменно. '
          'Так обе стороны будут понимать, когда что должно произойти.';
    }

    // --- Place ---
    if (has(['где состоится', 'место передачи', 'место встречи', 'где передад'])) {
      return 'Это место будет указано в договоре как место передачи. '
          'Так обе стороны будут понимать, где должна состояться сделка.';
    }
    if (has(['адрес', 'кадастров', 'где находится'])) {
      return 'Адрес нужен, чтобы в договоре было понятно, о каком именно объекте идёт речь. '
          'Он будет указан в описании предмета сделки.';
    }

    // --- The thing being sold/rented ---
    if (has(['vin', 'вин', 'номер двигателя', 'номер кузова', 'номер шасси', 'госномер', 'регистрационный номер'])) {
      return 'Этот номер помогает точно указать в договоре, о каком автомобиле идёт речь. '
          'Так его нельзя будет перепутать ни с каким другим.';
    }
    if (has(['марка', 'модель', 'год выпуска'])) {
      return 'Мы спрашиваем это, чтобы точно описать автомобиль в договоре. '
          'Обе стороны будут понимать, что именно продаётся.';
    }
    if (has(['площадь', 'комнат', 'этаж'])) {
      return 'Эти детали описывают объект в договоре. '
          'Так будет понятно, что именно передаётся, и не возникнет разночтений.';
    }

    // --- People ---
    if (has(['фио', 'ф.и.о', 'имя', 'фамили', 'паспорт', 'дата рождения'])) {
      return 'Мы спрашиваем это, чтобы в договоре было точно указано, кто участвует в сделке. '
          'Без этих данных договор нельзя будет подписать.';
    }
    if (has(['должност', 'позици', 'кем будет работать'])) {
      return 'Мы уточняем должность, чтобы в договоре было понятно, какую работу вы договорились выполнять. '
          'Она будет указана в разделе об обязанностях.';
    }
    if (has(['телефон', 'контакт', 'почт', 'email'])) {
      return 'Контакты нужны, чтобы стороны могли связаться друг с другом. '
          'Они будут указаны в конце договора.';
    }

    // --- Extra terms ---
    if (has(['особые услови', 'дополнительн', 'примечани', 'ещё что-то', 'пожелани'])) {
      return 'Здесь можно указать любые дополнительные договорённости. '
          'Они тоже будут включены в договор.';
    }

    // --- Calm human fallback ---
    return 'Мы спрашиваем это, чтобы договор точно отражал вашу договорённость. '
        'Ваш ответ будет вписан в соответствующий пункт документа.';
  }
}
